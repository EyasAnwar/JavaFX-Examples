package javafx.chapter15;

/**
 *
 * @author eyas
 */
public class Course {
    private double grade;
    private int hours;

    public Course(double grade, int hours) {
        this.grade = grade;
        this.hours = hours;
    }

    /**
     * @return the grade
     */
    public double getGrade() {
        return grade;
    }

    /**
     * @param grade the grade to set
     */
    public void setGrade(double grade) {
        this.grade = grade;
    }

    /**
     * @return the hours
     */
    public int getHours() {
        return hours;
    }

    /**
     * @param hours the hours to set
     */
    public void setHours(int hours) {
        this.hours = hours;
    }
}
